
USE dbepslaravel;

/* PROCEDIMIENTO CREAR ENTIDAD REGIONAL */
CREATE TABLE REGIONAL (

id_regional int AUTO_INCREMENT not null,
nombre_regional varchar(255) not null,
CONSTRAINT PK_regional primary key (id_regional));

/* PROCEDIMIENTO CREAR ENTIDAD ZONAL */
CREATE TABLE ZONAL (
id_zonal int AUTO_INCREMENT not null,
id_regional int not null,
zonal varchar(255) not null,
CONSTRAINT PK_zonal primary key (id_zonal),
CONSTRAINT FK_regional FOREIGN KEY (id_regional)REFERENCES REGIONAL (id_regional));

/* PROCEDIMIENTO CREAR ENTIDAD ESTADO */
CREATE TABLE ESTADO (
id_estado int not null,
estado varchar (15),
CONSTRAINT PK_estado primary key (id_estado));

/* PROCEDIMIENTO CREAR ENTIDAD IPS */
CREATE TABLE IPS (
cod_ips varchar(10)  NOT NULL,
nombre_ips varchar(255) NOT NULL,
razon_social varchar(255)NOT NULL,
id_zonal int NOT NULL,
id_estado int NOT NULL,
CONSTRAINT PK_ips primary key (cod_ips),
CONSTRAINT FK_zonal FOREIGN KEY (id_zonal)REFERENCES ZONAL (id_zonal),
CONSTRAINT FK_estado FOREIGN KEY (id_estado)REFERENCES ESTADO (id_estado));

/* PROCEDIMIENTO CREAR ENTIDAD PERFIL */
CREATE TABLE PERFIL(
id_perfil int not null,
perfil varchar (25)not null,
CONSTRAINT PK_perfil primary key (id_perfil));

/* PROCEDIMIENTO CREAR ENTIDAD USUARIO */
CREATE TABLE USUARIO(
id_usuario int AUTO_INCREMENT not null,
usuario int not null,
clave varchar(50),
nombres varchar(100) not null,
apellidos varchar(100) not null,
id_perfil int not null,
CONSTRAINT PK_usuario primary key (id_usuario),
CONSTRAINT FK_perfil FOREIGN KEY (id_perfil)REFERENCES PERFIL (id_perfil));

/* PROCEDIMIENTO CREAR ENTIDAD LISTA */
CREATE TABLE LISTA (
id_lista int AUTO_INCREMENT not null ,
id_regional int,
id_zonal int,
fecha_inicial date,
fecha_final date,
id_usuario int,
cod_ips varchar (10) not null, 
pregunta_1 varchar(20),
pregunta_2 varchar(20),
pregunta_3 varchar(20),
pregunta_4 varchar(20),
pregunta_5 varchar(20),
pregunta_6 varchar(20),
pregunta_7 int,
pregunta_8 int, 
pregunta_9 varchar(20),
pregunta_10 varchar(20),
pregunta_11 varchar(20),
pregunta_12 varchar(20),
pregunta_13 varchar(20),
pregunta_14 int,
pregunta_15 int,
pregunta_16 varchar(20),
pregunta_17 varchar(20),
pregunta_18 varchar(20),
pregunta_19 varchar(20),
pregunta_20 varchar(20),
observaciones varchar(255),
CONSTRAINT PK_lista primary key (id_lista),
CONSTRAINT FK_usuario FOREIGN KEY (id_usuario)REFERENCES USUARIO (id_usuario),
CONSTRAINT FK_ips FOREIGN KEY (cod_ips)REFERENCES IPS (cod_ips));

